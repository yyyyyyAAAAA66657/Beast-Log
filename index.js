// 단계별 안전 실행 (에러만 콘솔로, 화면 로그는 디버그일 때만)
function safeStep(label, fn) {
    try {
        fn();
        hotmicDebug('✅ ' + label);
    } catch (e) {
        console.error('[Hot Mic] ' + label + ' 에러:', e);
        hotmicDebug('❌ ' + label + ': ' + (e?.message || e), true);
    }
}

